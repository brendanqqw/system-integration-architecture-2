#VARIABLES
num1 = 20
num2 = 30

#ARITHMETIC
sum = num1 + num2
difference = num1 - num2
quotient = num1 / num2
product = num1 * num2

#ADDITION
print("The sum of", num1 ,"and" ,num2,sum)

#SUBTRACTION
print("The difference of", num1, "and", num2,difference)

#DIVIDE
print("The quotient of", num1, "and", num2,quotient)

#MULTIPLICATION
print("The product of", num1, "and", num2,product)
